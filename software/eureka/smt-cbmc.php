  <h1>SMT-CBMC: a STM-based Bounded Model Checker</h1>

<p>
  SMT-CBMC a bounded model checker for sequential programs.  It generalizes the encoding technique developed and implemented in CBMC so to generate an SMT formula instead of a SAT formula.   In this way SMT-CBMC leverages the stenghts of state-of-the-art SMT solvers, namely the ability to reason about complex data structures (e.g. numerical data, bit-vectors, lists, arrays). 
</p>

<p>
SMT-CBMC has been developed in the <a href="http://www.ai-lab.it" target="_parent">AI-Lab</a> at the <a href="http://www.unige.it" target="_parent">University of Genova</a> and
 has been partly supported by the <a href="http://www.miur.it" target="_parent">Italian Ministry of University and Research</a>, in the context of the <a href="http://profs.sci.univr.it/~bonacina/prin2003.html" target="_parent">PRIN Project no. 2003-097383</a>.
</p>

